export const normalizeLanguage = (lang) => {
  const value = String(lang || '').toLowerCase();
  return value.startsWith('de') ? 'de' : 'ru';
};

export const getPreferredLanguage = (telegramApp) => {
  const tgCode = telegramApp?.initDataUnsafe?.user?.language_code || telegramApp?.initDataUnsafe?.language_code;
  if (tgCode) return normalizeLanguage(tgCode);
  if (typeof navigator !== 'undefined' && navigator.language) {
    return normalizeLanguage(navigator.language);
  }
  return 'ru';
};

export const createTranslator = (lang) => {
  const current = dictionaries[normalizeLanguage(lang)] || dictionaries.ru;
  const fallback = dictionaries.ru;
  return (key, vars = {}) => {
    const template = current[key] ?? fallback[key] ?? key;
    return String(template).replace(/\{\{(\w+)\}\}/g, (_m, token) => {
      const value = vars[token];
      return value === undefined || value === null ? '' : String(value);
    });
  };
};
